			<li></li>
			<li>2.2.4 禁止签名档内涉嫌发布任何性质广告, 包括文字、外链地址、图片等; </li>
			<li></li>
			<li>2.2.5 禁止使用侮辱、攻击、丑化其他会员或管理员ID等易引起纷争的头像和签名;</li>
			<li></li>
			<li>2.2.6 禁止使用不文明、不健康, 或带有攻击性、侮辱性的、猥亵的、挑衅等内容的文字, 做为头像和签名;</li>
