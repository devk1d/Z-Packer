			<li></li>
			<li>2.1.1 禁止使用任何国家政党或领导人或其他知名人士的真实姓名、字号、艺名、笔名作为用户名或昵称;（如：习主席）</li>
			<li></li>
			<li>2.1.2 禁止使用任何国家机构或其他机构的名称作为用户名或昵称;（如：国家安全部）</li>
			<li></li>
			<li>2.1.3 禁止使用反动、政治、宗教性质等违反国家法规的文字, 做为用户名或昵称;（如：达赖）</li>
			<li></li>
			<li>2.1.4 禁止使用不文明、不健康, 或带有攻击性、侮辱性的、猥亵的、挑衅等内容的文字, 做为用户名或昵称;（如：XX流氓）</li>
			<li></li>
			<li>2.1.5 禁止使用带有广告性质的用户名或昵称;（如：XX商城）</li>
			<li></li>
			<li>2.1.6 禁止使用带有种族歧视、地域歧视性质的用户名或昵称;（如：该死的XX小偷）</li>
			<li></li>
			<li>2.1.7 禁止使用侮辱、攻击、丑化其他会员或管理员ID和ZEALER等易引起纷争的用户名或昵称;（例：SBXX）</li>
			<li></li>
			<li>2.1.8 使用马甲辱骂管理员和会员者, 在不通知的情况下直接永久禁访马甲账号, 对主号严厉警告, 屡犯不改的禁止所有ID账号, 禁止IP;</li>
			<li></li>
			<li>2.1.9 使用马甲恶意扰乱网站秩序者, 直接禁止其所有帐号。</li>
			<li></li>
			<li>2.2 会员头像及签名规定</li>
			<li></li>
			<li>2.2.1 禁止使用和篡改受国家法规保护的图案, 如人民币、国徽、国旗、党和国家各级领导人或其他名人的照片做为头像和签名;</li>
			<li></li>
			<li>2.2.2 禁止使用公众认可性的, 涉嫌低级趣味、侮辱、影射等一切有违国家尊严和文明道德标准的手势、符号、英文等图片做为头像和签名;</li>
			<li></li>
