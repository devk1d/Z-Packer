			<li>3. 会员问答社区内容规定</li>
			<li></li>
			<li>3.1.1 严禁发表对会员进行人身攻击, 谩骂, 挑衅的提问、回答和评论;</li>
			<li></li>
			<li>3.1.2 严禁发表网赚、赌博（如各类非法彩票、跑马等）类提问、回答和评论;</li>
			<li></li>
			<li>3.1.3 严禁发表色情及带有性描写的提问、回答和评论;</li>
			<li></li>
			<li>3.1.4 严禁发表与政治有关的提问、回答和评论;</li>
			<li></li>
			<li>3.1.5 严禁发布不明链接、虚拟货币交易、帐号交易等有可能使其他会员上当受骗的提问、回答和评论;</li>
			<li></li>
			<li>3.1.6 禁止发表含有包括但不限于“垃圾”、“SB”等侮辱性词眼的提问、回答和评论;</li>
			<li></li>
			<li>3.1.7 严禁张贴未经当事人许可的任何私人信息;</li>
			<li></li>
			<li>3.1.8 如果转载机型讨论、心得体会和技术资源请注明原作者或转载来源, 转载不注明来源的将被删帖处理;</li>
			<li></li>
			<li>3.1.9 在回答会员的问题时, 回答应尽量详细, 请尽量带有具体的操作过程和分析;</li>
			<li></li>
			<li>3.2.0 很多问题都是已经解决或被回答多次的, 本网站拥有强大的搜索功能, 请提问前先使用搜索功能搜索问答或者标签;</li>
			<li></li>
			<li>3.2.1 禁止在任何版块发布非ZEALER官方的相关消息, 包括冒充官方的群号码、群名称, 对于非官方群的纠纷, ZEALER官方一概不予受理。</li>
			<li>&nbsp;</li>
			<li></li>
			<li>4. 灌水帖、垃圾帖及广告帖的界定</li>
			<li></li>
			<li>4.1 灌水帖</li>
			<li></li>
			<li>4.1.1 只有标点/符号/表情/纯引用的纯灌水提问、回答和评论;</li>
			<li></li>
			<li>4.1.2 多个提问相同内容回复;</li>
			<li></li>
			<li>4.1.4 各版块可根据具体版规定义灌水帖;</li>
			<li></li>
			<li>4.1.5 在10分钟内, 问答社区同一页面上的十个问答贴为同一ID或十个回答贴最后回答均为同一ID为刷屏;</li>
			<li></li>
			<li>4.1.6 灌水帖包括但不限于以上定义, 本网站管理有权对灌水行为进行判断。</li>
			<li></li>
			<li>4.2 垃圾帖</li>
			<li></li>
			<li>4.2.1 标题不明, 不知所以然的提问、回答和评论;</li>
			<li></li>
			<li>4.2.2 同一版面内容重复的提问、回答和评论;</li>
			<li></li>
			<li>4.2.3 空贴、试发贴等无任何意义的提问、回答和评论;</li>
			<li></li>
			<li>4.2.4 垃圾帖包括但不限于以上定义, 本网站管理有权对垃圾帖进行判断。</li>
			<li></li>
			<li>4.3 广告帖</li>
			<li></li>
			<li>4.3.1 欺骗点击类的地址链接;</li>
			<li></li>
			<li>4.3.2 主要内容为网址链接或QQ帐号宣传的提问、回答和评论;</li>
			<li></li>
			<li>4.3.3 包含为他人或自己谋取利益的链接的提问、回答和评论;</li>
			<li></li>
			<li>4.3.4 包含不健康或违反规定的链接的提问、回答和评论;</li>
			<li></li>
			<li>4.3.5 跨多版块发同一资源（软件、游戏等）即被定义为恶意推广;</li>
			<li></li>
			<li>4.3.6 提问标题及描述不相符即被定义为虚假描述;</li>
			<li>&nbsp;</li>
