			<li></li>
			<li>5.1 会员违规时的处理手段</li>
			<li></li>
			<li>5.1.1 扣除违规帐号积分（有积分制度后）;</li>
			<li></li>
			<li>5.1.2 屏蔽或删除违规内容（删除会扣除相应积分）;</li>
			<li></li>
			<li>5.1.3 对违规帐号给予警告操作（警告三次禁言30天）;</li>
			<li></li>
			<li>5.1.4 对违规帐号给予限时或永久禁止发言操作;</li>
			<li></li>
			<li>5.1.5 对违规帐号给予限时或永久禁止访问操作;</li>
			<li></li>
			<li>5.1.6 对违规者给予限时或永久禁止IP操作;</li>
			<li></li>
			<li>5.1.7 帐号被封禁期间允许注册新帐号进行申诉。</li>
			<li></li>
			<li>5.2 从轻处罚</li>
			<li></li>
			<li>5.2.1 网站管理人员对于违规现象可视情况给予不同程度的处理, 对于以下情况可以从轻或免除处罚;</li>
			<li></li>
			<li>5.2.2 新来网站的初犯者, 能认识到错误并及时改正的;</li>
			<li></li>
			<li>5.2.3 老会员或有巨大贡献会员如不慎违规, 认识错误并及时更正的;</li>
			<li></li>
			<li>5.2.4 由于误操作, 或网络问题导致一些“假”灌水行为证实后。</li>
			<li></li>
			<li>5.3 从重处罚</li>
			<li></li>
			<li>5.3.1 怀有侥幸心理而明知故犯, 明知违规却用掩耳盗铃的手段逃避或打擦边球的;</li>
			<li></li>
			<li>5.3.2 以各种形式钻网站漏洞, 且还振振有词, 被警告之后仍然不听劝告, 继续一意孤行的;</li>
			<li></li>
			<li>5.3.3 屡次违规的, 将从重处理, 删帖后注册马甲继续违规的;</li>
			<li></li>
			<li>5.3.4 因为对于管理者按规定的处罚、处理不满而恶意违规破坏秩序的;</li>
			<li></li>
			<li>5.3.5 由于对管理人员不满辱骂管理人员, 此种行径被视为是蓄意挑战网站, 意图扰乱网站正常运作的;</li>
			<li></li>
			<li>5.3.6 任何人对管理人员进行任何形式的人身攻击, 一经发现, 必将从严处理;</li>
			<li></li>
			<li>5.3.7 会员挑拨离间, 意图扰乱官方与会员之间关系的;</li>
			<li></li>
			<li>5.3.8 以某种理由煽动其他会员离开网站的。</li>
			<li></li>
			<li>5.4 会员管理</li>
			<li></li>
			<li>5.4.1 会员有权对网站内的违规内容进行监督、批评和投诉, 有权提出自己对网站建设的建议和意见。</li>
